#include<iostream>
#include <list>
 
using namespace std;
 
// Lop này bieu dien mot do thi có huong bang cách su dung 
// bieu dien danh sách ke
class Graph
{
    int V;    // so dinh
 
    // Con tro den mot mang có chua // danh sách lien ke
    list<int> *adj;  
public:
    Graph(int V);  // Constructor
 
    // function to add an edge to graph
    void addEdge(int v, int w);
 
    // prints BFS traversal from a given source s
    void BFS(int s); 
};
 
Graph::Graph(int V)
{
    this->V = V;
    adj = new list<int>[V];
}
 
void Graph::addEdge(int v, int w)
{
    adj[v].push_back(w); // Add w to v’s list.// Thêm w vào danh sách cua v
}
 
void Graph::BFS(int s)
{
    // Ðánh dau tat ca các dinh là không duoc tham
    bool *visited = new bool[V];
    for(int i = 0; i < V; i++)
        visited[i] = false;
 
    // Tao hàng doi cho BFS
    list<int> queue;
 
    // Ðánh dau nút hien tai là dã truy cap và xep nó vào hàng doi
    visited[s] = true;
    queue.push_back(s);
 
    // 'i' se duoc su dung de lay tat ca các dinh // lien ke cua mot dinh
    list<int>::iterator i;
 
    while(!queue.empty())
    {
        // Xep hàng doi mot dinh tu hàng doi và in nó
        s = queue.front();
        cout << s << " ";
        queue.pop_front();
 
        // Lay tat ca các dinh lien ke cua dinh s. Neu mot vùng lien ke chua duoc truy cap, 
		// sau dó dánh dau nó dã duoc truy cap và xep hàng
        for (i = adj[s].begin(); i != adj[s].end(); ++i)
        {
            if (!visited[*i])
            {
                visited[*i] = true;
                queue.push_back(*i);
            }
        }
    }
}
 
// Driver program to test methods of graph class
int main()
{
    // Create a graph given in the above diagram
    Graph g(4);
    g.addEdge(0, 1);
    g.addEdge(0, 2);
    g.addEdge(1, 2);
    g.addEdge(2, 0);
    g.addEdge(2, 3);
    g.addEdge(3, 3);
 
    cout << "Following is Breadth First Traversal "
         << "(starting from vertex 2) \n";
    g.BFS(2);
 
    return 0;
}
